# Conditionals
x = 4

if x < 5:
    print(True)

# if else
if x > 5:
    print(True)
else:
    print(False)

# Elif
color = 'red'

if color == 'red':
    print('whaat')
elif color == 'blue':
    print('whaa waaat')
else:
    print('wha wha whaaaaAAt')

# And
if color == 'red' and x < 10:
    print('shut up')

# str interpolation
print('the car is {0}'.format(color))  # -> The car is red
